#!/usr/bin/env node
/**
 * Scans public/campus/ for raster (webp/png) + SVG assets,
 * validates completeness, and regenerates campus-asset-inventory.generated.ts
 *
 * Usage: npm run campus:validate
 */
import { existsSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const CAMPUS = join(ROOT, "public", "campus");
const OUT = join(ROOT, "src", "lib", "game", "campus-asset-inventory.generated.ts");

const VARIANTS = ["stadium", "academy", "office", "finance", "medical", "gym"];
const TIERS = [1, 2, 3];
const STAGES = [1, 2, 3, 4, 5];

function exists(rel) {
  return existsSync(join(CAMPUS, rel));
}

function rasterExt(basePath) {
  if (exists(`${basePath}.webp`)) return "webp";
  if (exists(`${basePath}.png`)) return "png";
  if (exists(`${basePath}.webp.png`)) return "webp.png";
  return null;
}

function scanSlot(basePath) {
  const raster = rasterExt(basePath);
  const svg = exists(`${basePath}.svg`);
  return { webp: raster === "webp", png: raster === "png" || raster === "webp.png", raster, svg };
}

function scanVariant(variant) {
  const idle = {};
  const construction = {};
  let rasterCount = 0;
  const requiredRaster = 8;

  for (const tier of TIERS) {
    const slot = scanSlot(`buildings/${variant}/idle/tier-${tier}`);
    idle[`tier${tier}`] = slot;
    if (slot.raster) rasterCount += 1;
  }

  for (const stage of STAGES) {
    const slot = scanSlot(`buildings/${variant}/construction/stage-${stage}`);
    construction[`stage${stage}`] = slot;
    if (slot.raster) rasterCount += 1;
  }

  const idleRasterCount = TIERS.filter((t) => idle[`tier${t}`].raster).length;
  const constructionRasterCount = STAGES.filter((s) => construction[`stage${s}`].raster).length;

  return {
    idle,
    construction,
    aiVerified: rasterCount === requiredRaster,
    aiIdleVerified: idleRasterCount === TIERS.length,
    aiConstructionVerified: constructionRasterCount === STAGES.length,
    rasterCount,
    requiredRaster,
    idleRasterCount,
    constructionRasterCount,
  };
}

const masterRaster = rasterExt("bg/master");
const masterSvg = exists("bg/master.svg");
const excavatorRaster = rasterExt("animations/excavator-sheet");
const excavatorSvg = exists("animations/excavator-sheet.svg");
const truckRaster = rasterExt("animations/dump-truck-sheet");
const truckSvg = exists("animations/dump-truck-sheet.svg");

const variants = {};
const aiVerified = [];
const aiIdleVerified = [];

for (const variant of VARIANTS) {
  const data = scanVariant(variant);
  variants[variant] = data;
  if (data.aiVerified) aiVerified.push(variant);
  if (data.aiIdleVerified) aiIdleVerified.push(variant);
}

const missing = [];
if (!masterRaster) missing.push("bg/master.webp (or master.png)");

for (const variant of VARIANTS) {
  const data = variants[variant];
  if (data.aiVerified) continue;
  for (const tier of TIERS) {
    if (!data.idle[`tier${tier}`].raster) {
      missing.push(`buildings/${variant}/idle/tier-${tier}.webp|.png`);
    }
  }
  for (const stage of STAGES) {
    if (!data.construction[`stage${stage}`].raster) {
      missing.push(`buildings/${variant}/construction/stage-${stage}.webp|.png`);
    }
  }
}

if (!excavatorRaster) missing.push("animations/excavator-sheet.webp|.png");
if (!truckRaster) missing.push("animations/dump-truck-sheet.webp|.png");

const inventoryTs = `// AUTO-GENERATED by scripts/validate-campus-assets.mjs — do not edit manually
// Run: npm run campus:validate

import type { CampusBuildingVariant } from "./campus-visual-tiers";

export type CampusAssetFileState = {
  webp: boolean;
  png: boolean;
  raster: string | null;
  svg: boolean;
};

export type CampusVariantInventory = {
  idle: Record<"tier1" | "tier2" | "tier3", CampusAssetFileState>;
  construction: Record<
    "stage1" | "stage2" | "stage3" | "stage4" | "stage5",
    CampusAssetFileState
  >;
  aiVerified: boolean;
  aiIdleVerified: boolean;
  aiConstructionVerified: boolean;
  rasterCount: number;
  requiredRaster: number;
  idleRasterCount: number;
  constructionRasterCount: number;
};

export const CAMPUS_ASSET_INVENTORY = ${JSON.stringify(
  {
    masterBackground: {
      webp: masterRaster === "webp",
      png: masterRaster === "png" || masterRaster === "webp.png",
      raster: masterRaster,
      svg: masterSvg,
    },
    animations: {
      excavator: {
        webp: excavatorRaster === "webp",
        png: excavatorRaster === "png" || excavatorRaster === "webp.png",
        raster: excavatorRaster,
        svg: excavatorSvg,
      },
      dumpTruck: {
        webp: truckRaster === "webp",
        png: truckRaster === "png" || truckRaster === "webp.png",
        raster: truckRaster,
        svg: truckSvg,
      },
    },
    variants,
  },
  null,
  2
)} as const;

export const AI_VERIFIED_CAMPUS_VARIANTS = ${JSON.stringify(aiVerified)} as CampusBuildingVariant[];

export const AI_IDLE_VERIFIED_CAMPUS_VARIANTS = ${JSON.stringify(aiIdleVerified)} as CampusBuildingVariant[];

export const HAS_AI_MASTER_BACKGROUND = ${Boolean(masterRaster)};
`;

writeFileSync(OUT, inventoryTs, "utf8");

console.log("Campus asset inventory synced.\n");
console.log(
  `AI idle verified (${aiIdleVerified.length}/6): ${aiIdleVerified.join(", ") || "none"}`
);
console.log(
  `AI full verified (${aiVerified.length}/6): ${aiVerified.join(", ") || "none"}`
);
console.log(`Master raster: ${masterRaster ?? "missing"}`);
console.log(
  `Animation raster: excavator=${excavatorRaster ?? "no"}, truck=${truckRaster ?? "no"}\n`
);

if (aiIdleVerified.length > 0) {
  console.log("Idle IA preview enabled for:", aiIdleVerified.join(", "));
}

if (missing.length === 0) {
  console.log("All required raster assets present.");
} else {
  console.log(`Missing ${missing.length} raster file(s) for full rollout:`);
  for (const path of missing.slice(0, 15)) {
    console.log(`  - ${path}`);
  }
  if (missing.length > 15) {
    console.log(`  ... and ${missing.length - 15} more`);
  }
}

process.exit(0);
